#USING THE MATH SQUARE

import math
class Number_sqrt:
    def __init__(self,numbers):
        self.numbers = numbers

    def square_number(self):
        return math.sqrt(self.numbers)
        
p1 = Number_sqrt(16)
print(p1.square_number())
   
























